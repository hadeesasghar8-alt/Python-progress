"""
To join tuples you use the + operator"""
tuple = ("Dante", "Vergil", "Nero")
tuple2 = ("Lady", "Trish", "V")
tuple3 = tuple + tuple2
print(tuple3)
"""
You can also multiply variables in the tuples by using the * operator"""
fruits = ("apple", "Bannana", "Pear")
mytuple = fruits * 2
print(mytuple)