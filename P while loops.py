# While loops can execute a set of conditions onlywhen a condition is true.
i = 5
while i < 7:
    print(i)
    i += 5
# The increment at the end stops the loop for continuing forever.
i = 50
while i < 70:
    print(i)
    if i == 55:
        break
    i += 50    
# The continue statment can stop the current iteration and continue with the next
MP = 50
while MP < 60:
    print(MP)
    if MP == 50:
        continue
    print(MP)
